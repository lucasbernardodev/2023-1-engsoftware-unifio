// 5) Desenvolva um algoritmo que peça ao usuário 4 números, calcule o quadrado de
// todos eles, e some o quadrado de todos os números. O algoritmo deve exibir a
// soma 

var numero1 = prompt("Digite o primeiro número")
var numero2 = prompt("Digite o segundo número")
var numero3 = prompt("Digite o terceiro número")
var numero4 = prompt("Digite o quarto número")

var quadrado1 = numero1 * numero1
var quadrado2 = numero2 * numero2
var quadrado3 = numero3 * numero3
var quadrado4 = numero4 * numero4

var soma = quadrado1 + quadrado2 + quadrado3 + quadrado4

alert("A soma dos quadrados é: " + soma)
