// 6) - Quando nós brasileiros visitamos os EUA, o primeiro impacto é a temperatura,
// que é medida em Fahrenheit. Faça um algoritmo que ajude os turistas, onde seja
// informada a temperatura em Fahrenheit e seja mostrada a temperatura em graus
// centígrados 

var fahrenheit = prompt("Digite a temperatura em Fahrenheit")
var celsius = (fahrenheit - 32) * 5 / 9

alert("A temperatura em graus centígrados é: " + celsius)
