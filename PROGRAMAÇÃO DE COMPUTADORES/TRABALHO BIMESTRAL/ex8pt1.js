// 8 - Crie um algorítmo, onde uma pessoa informa um número, e o algorítmo calcula
// e informa o seu sucessor e seu antecessor

var numero = prompt("Digite um número")
var sucessor = parseInt(numero) + 1
var antecessor = parseInt(numero) - 1

alert("O sucessor é: " + sucessor + " e o antecessor é: " + antecessor)
